    /** Для выполнения этого упражнения вам придется просмотреть исходный код
     класса java.util.Scanner. Если при использовании класса Scanner операция
     ввода завершается неудачно, в этом классе перехватывается исключение в связи
     с ошибками ввода данных и закрывается ресурс, из которого осуществляется
     ввод. Что произойдет, если ири закрытии ресурса будет сгенерировано исключение?

    Решение: в этом случае
     сгенерированное исключение передается вызывающему коду, когда блок оператора
     try завершается нормально. Но если сгенерировано другое исключение, приводящее
     к вызову методов close() используемых ресурсов, а один из этих методов генерирует
     исключение, то такое исключение, вероятнее всего, окажется менее важным, чем первоначальное.
     В таком случае первоначальное исключение генерируется повторно, а исключения,
     возникающие в результате вызова методов close(), перехватываются и присоединяются как " подавленные **/

